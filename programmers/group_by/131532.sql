-- 문제: 년, 월, 성별 별 상품 구매 회원 수 구하기
-- 링크: https://school.programmers.co.kr/learn/courses/30/lessons/131532

SELECT
    YEAR(S.SALES_DATE) AS `YEAR`,
    MONTH(S.SALES_DATE) AS `MONTH`,
    U.GENDER AS `GENDER`,
    COUNT(DISTINCT U.USER_ID) AS USERS
FROM
    USER_INFO U
JOIN
    ONLINE_SALE S ON U.USER_ID = S.USER_ID
WHERE
    U.GENDER IS NOT NULL
GROUP BY
    YEAR(S.SALES_DATE),
    MONTH(S.SALES_DATE),
    U.GENDER
ORDER BY
    YEAR(S.SALES_DATE),
    MONTH(S.SALES_DATE),
    U.GENDER
